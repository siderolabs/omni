const (
	definitionsRefPrefix = "#/definitions/"
	pathSeparator        = "."
	commentWrapColumn    = 110

	generatedConfigComment = "# Code generated by helmvaluesgen; DO NOT EDIT."
	beginConfigMarker      = "# ---------- BEGIN helmvaluesgen config ----------"
	endConfigMarker        = "# ---------- END helmvaluesgen config ----------"

	yamlStringTag = "!!str"
	yamlNullTag   = "!!null"
	yamlBoolTag   = "!!bool"
	yamlIntTag    = "!!int"
	yamlFloatTag  = "!!float"
	yamlSeqTag    = "!!seq"
	yamlMapTag    = "!!map"

	schemaObjectType = "object"

	yamlNullValue  = "null"
	yamlFalseValue = "false"
)

// Run updates the top-level config section in values.yaml from the config JSON
// schema and the chart override file.
//
// Active values in the override file become chart defaults. All other schema
// leaf values are rendered as commented examples, using the schema default when
// available or a type-based placeholder otherwise.
func Run(schemaPath string, overridesPath string, valuesPath string) error {
	if schemaPath == "" {
		return fmt.Errorf("no schema provided, use --schema flag")
	}

	if overridesPath == "" {
		return fmt.Errorf("no overrides provided, use --overrides flag")
	}

	if valuesPath == "" {
		return fmt.Errorf("no values provided, use --values flag")
	}

	schema, err := loadSchema(schemaPath)
	if err != nil {
		return err
	}

	overrides, err := loadOverrides(overridesPath)
	if err != nil {
		return err
	}

	if err = validateOverrides(schema, overrides); err != nil {
		return err
	}

	block, err := renderConfigBlock(schema, overrides)
	if err != nil {
		return err
	}

	valuesData, err := os.ReadFile(valuesPath)
	if err != nil {
		return fmt.Errorf("failed to read values file %q: %w", valuesPath, err)
	}

	updated, err := replaceConfigBlock(string(valuesData), block)
	if err != nil {
		return err
	}

	if err = os.WriteFile(valuesPath, []byte(updated), 0o644); err != nil {
		return fmt.Errorf("failed to write values file %q: %w", valuesPath, err)
	}

	return nil
}

type schema struct {
	root        *schemaNode
	definitions map[string]*schemaNode
}

type schemaNode struct {
	defaultValue         *yaml.Node
	definitions          map[string]*schemaNode
	additionalProperties *schemaNode
	items                *schemaNode
	typ                  string
	ref                  string
	description          string
	properties           []schemaProperty
}

type schemaProperty struct {
	node *schemaNode
	name string
}

func loadSchema(path string) (*schema, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("failed to read schema file %q: %w", path, err)
	}

	var doc yaml.Node
	if err = yaml.Unmarshal(data, &doc); err != nil {
		return nil, fmt.Errorf("failed to parse schema file %q: %w", path, err)
	}

	root := documentNode(&doc)
	if root == nil {
		return nil, fmt.Errorf("schema file %q is empty", path)
	}

	parsed := parseSchemaNode(root)

	return &schema{
		root:        parsed,
		definitions: parsed.definitions,
	}, nil
}

func parseSchemaNode(node *yaml.Node) *schemaNode {
	parsed := &schemaNode{}

	if node == nil || node.Kind != yaml.MappingNode {
		return parsed
	}

	for i := 0; i+1 < len(node.Content); i += 2 {
		key, value := node.Content[i], node.Content[i+1]

		switch key.Value {
		case "$ref":
			parsed.ref = scalarString(value)
		case "type":
			parsed.typ = scalarString(value)
		case "description":
			parsed.description = scalarString(value)
		case "default":
			parsed.defaultValue = cloneYAMLNode(value)
		case "properties":
			parsed.properties = parseSchemaProperties(value)
		case "definitions":
			parsed.definitions = parseSchemaDefinitions(value)
		case "additionalProperties":
			if value.Kind == yaml.MappingNode {
				parsed.additionalProperties = parseSchemaNode(value)
			}
		case "items":
			parsed.items = parseSchemaNode(value)
		}
	}

	return parsed
}

func parseSchemaProperties(node *yaml.Node) []schemaProperty {
	if node == nil || node.Kind != yaml.MappingNode {
		return nil
	}

	properties := make([]schemaProperty, 0, len(node.Content)/2)

	for i := 0; i+1 < len(node.Content); i += 2 {
		properties = append(properties, schemaProperty{
			name: node.Content[i].Value,
			node: parseSchemaNode(node.Content[i+1]),
		})
	}

	return properties
}

func parseSchemaDefinitions(node *yaml.Node) map[string]*schemaNode {
	if node == nil || node.Kind != yaml.MappingNode {
		return nil
	}

	definitions := make(map[string]*schemaNode, len(node.Content)/2)

	for i := 0; i+1 < len(node.Content); i += 2 {
		definitions[node.Content[i].Value] = parseSchemaNode(node.Content[i+1])
	}

	return definitions
}

func resolveSchemaNode(node *schemaNode, definitions map[string]*schemaNode) (*schemaNode, error) {
	if node == nil || node.ref == "" {
		return node, nil
	}

	if !strings.HasPrefix(node.ref, definitionsRefPrefix) {
		return nil, fmt.Errorf("unsupported schema ref %q", node.ref)
	}

	refName := strings.TrimPrefix(node.ref, definitionsRefPrefix)

	base := definitions[refName]
	if base == nil {
		return nil, fmt.Errorf("schema ref %q not found", node.ref)
	}

	return mergeSchemaNodes(base, node), nil
}

func mergeSchemaNodes(base *schemaNode, overlay *schemaNode) *schemaNode {
	merged := cloneSchemaNode(base)

	if overlay.typ != "" {
		merged.typ = overlay.typ
	}

	if overlay.description != "" {
		merged.description = overlay.description
	}

	if overlay.defaultValue != nil {
		merged.defaultValue = cloneYAMLNode(overlay.defaultValue)
	}

	if overlay.additionalProperties != nil {
		merged.additionalProperties = cloneSchemaNode(overlay.additionalProperties)
	}

	if overlay.items != nil {
		merged.items = cloneSchemaNode(overlay.items)
	}

	for _, overlayProperty := range overlay.properties {
		found := false

		for i := range merged.properties {
			if merged.properties[i].name == overlayProperty.name {
				merged.properties[i].node = mergeSchemaNodes(merged.properties[i].node, overlayProperty.node)
				found = true

				break
			}
		}

		if !found {
			merged.properties = append(merged.properties, schemaProperty{
				name: overlayProperty.name,
				node: cloneSchemaNode(overlayProperty.node),
			})
		}
	}

	return merged
}

func cloneSchemaNode(node *schemaNode) *schemaNode {
	if node == nil {
		return nil
	}

	cloned := *node

	cloned.defaultValue = cloneYAMLNode(node.defaultValue)
	cloned.additionalProperties = cloneSchemaNode(node.additionalProperties)
	cloned.items = cloneSchemaNode(node.items)

	if len(node.properties) > 0 {
		cloned.properties = make([]schemaProperty, 0, len(node.properties))

		for _, property := range node.properties {
			cloned.properties = append(cloned.properties, schemaProperty{
				name: property.name,
				node: cloneSchemaNode(property.node),
			})
		}
	}

	if len(node.definitions) > 0 {
		cloned.definitions = make(map[string]*schemaNode, len(node.definitions))

		for name, definition := range node.definitions {
			cloned.definitions[name] = cloneSchemaNode(definition)
		}
	}

	return &cloned
}

type overrides struct {
	// omit contains schema paths that should not appear in the chart at all.
	omit map[string]bool
	// values contains active chart defaults from the overrides file.
	values map[string]*yaml.Node
	// examples contains commented examples for inactive leaves or active values
	// that benefit from an inline sample.
	examples map[string]*yaml.Node
	// comments contains chart-specific description overrides keyed by schema
	// path. When absent, the schema description is used.
	comments map[string]string
}

func loadOverrides(path string) (*overrides, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("failed to read overrides file %q: %w", path, err)
	}

	var doc yaml.Node
	if err = yaml.Unmarshal(data, &doc); err != nil {
		return nil, fmt.Errorf("failed to parse overrides file %q: %w", path, err)
	}

	root := documentNode(&doc)
	if root == nil {
		return nil, fmt.Errorf("overrides file %q is empty", path)
	}

	result := &overrides{
		omit:     map[string]bool{},
		values:   map[string]*yaml.Node{},
		examples: map[string]*yaml.Node{},
		comments: map[string]string{},
	}

	for i := 0; i+1 < len(root.Content); i += 2 {
		key, value := root.Content[i], root.Content[i+1]

		switch key.Value {
		case "omit":
			if err = parseOmit(value, "", result.omit); err != nil {
				return nil, fmt.Errorf("invalid omit section: %w", err)
			}
		case "config":
			if err = parseConfigOverrides(value, "", result.values, result.comments); err != nil {
				return nil, fmt.Errorf("invalid config section: %w", err)
			}
		case "comments":
			if err = parseComments(value, "", result.comments); err != nil {
				return nil, fmt.Errorf("invalid comments section: %w", err)
			}
		case "examples":
			if err = parseExampleOverrides(value, "", result.examples); err != nil {
				return nil, fmt.Errorf("invalid examples section: %w", err)
			}
		default:
			return nil, fmt.Errorf("unknown overrides section %q", key.Value)
		}
	}

	return result, nil
}

func validateOverrides(schema *schema, overrides *overrides) error {
	for path, value := range overrides.values {
		node, err := schema.nodeForPath(path)
		if err != nil {
			return fmt.Errorf("invalid config override %q: %w", path, err)
		}

		// parseConfigOverrides stores mapping nodes at every prefix so comments
		// can attach to them. A structured object is valid only when it has leaf
		// descendants; otherwise it would activate an empty branch in values.yaml.
		if node.isStructuredObject() && value.Kind == yaml.MappingNode && !hasDescendant(overrides.values, path) {
			return fmt.Errorf("invalid config override %q: structured object override has no leaf values", path)
		}
	}

	for path := range overrides.omit {
		if _, err := schema.nodeForPath(path); err != nil {
			return fmt.Errorf("invalid omit path %q: %w", path, err)
		}
	}

	for path, value := range overrides.examples {
		node, err := schema.nodeForPath(path)
		if err != nil {
			return fmt.Errorf("invalid example override %q: %w", path, err)
		}

		if node.isStructuredObject() && value.Kind == yaml.MappingNode && !hasDescendant(overrides.examples, path) {
			return fmt.Errorf("invalid example override %q: structured object example has no leaf values", path)
		}
	}

	for path := range overrides.comments {
		if _, err := schema.nodeForPath(path); err != nil {
			return fmt.Errorf("invalid comment override %q: %w", path, err)
		}
	}

	return nil
}

func (schema *schema) nodeForPath(path string) (*schemaNode, error) {
	node := schema.root

	if path == "" {
		return resolveSchemaNode(node, schema.definitions)
	}

	for part := range strings.SplitSeq(path, pathSeparator) {
		resolved, err := resolveSchemaNode(node, schema.definitions)
		if err != nil {
			return nil, err
		}

		var next *schemaNode

		for _, property := range resolved.properties {
			if property.name == part {
				next = property.node

				break
			}
		}

		if next == nil && resolved.additionalProperties != nil {
			next = resolved.additionalProperties
		}

		if next == nil {
			return nil, fmt.Errorf("path not found in schema")
		}

		node = next
	}

	return resolveSchemaNode(node, schema.definitions)
}

func parseExampleOverrides(node *yaml.Node, prefix string, examples map[string]*yaml.Node) error {
	if node == nil || node.Kind == 0 {
		return nil
	}

	if node.Kind != yaml.MappingNode {
		if prefix == "" {
			return fmt.Errorf("expected mapping, got %s", node.ShortTag())
		}

		examples[prefix] = cloneYAMLNode(node)

		return nil
	}

	for i := 0; i+1 < len(node.Content); i += 2 {
		key, value := node.Content[i], node.Content[i+1]
		if key.Kind != yaml.ScalarNode {
			return fmt.Errorf("expected scalar example key, got %s", key.ShortTag())
		}

		path := pathJoin(prefix, key.Value)
		examples[path] = cloneYAMLNode(value)

		if value.Kind == yaml.MappingNode {
			if err := parseExampleOverrides(value, path, examples); err != nil {
				return err
			}

			continue
		}
	}

	return nil
}

//nolint:gocognit
func parseOmit(node *yaml.Node, prefix string, target map[string]bool) error {
	if node == nil || node.Kind == 0 {
		return nil
	}

	switch node.Kind {
	case yaml.SequenceNode:
		for _, item := range node.Content {
			if item.Kind != yaml.ScalarNode {
				return fmt.Errorf("expected scalar path, got %s", item.ShortTag())
			}

			target[pathJoin(prefix, item.Value)] = true
		}
	case yaml.MappingNode:
		for i := 0; i+1 < len(node.Content); i += 2 {
			key, value := node.Content[i], node.Content[i+1]
			if key.Kind != yaml.ScalarNode {
				return fmt.Errorf("expected scalar path, got %s", key.ShortTag())
			}

			path := pathJoin(prefix, key.Value)
			if value.Kind == yaml.MappingNode {
				if err := parseOmit(value, path, target); err != nil {
					return err
				}

				continue
			}

			if value.Kind != yaml.ScalarNode || value.ShortTag() != yamlBoolTag {
				return fmt.Errorf("omit path %q must be true, false, or a nested mapping", path)
			}

			omit, err := strconv.ParseBool(value.Value)
			if err != nil {
				return fmt.Errorf("omit path %q must be true or false: %w", path, err)
			}

			if !omit {
				continue
			}

			target[path] = true
		}
	default:
		return fmt.Errorf("expected sequence or mapping, got %s", node.ShortTag())
	}

	return nil
}

func parseConfigOverrides(node *yaml.Node, prefix string, values map[string]*yaml.Node, comments map[string]string) error {
	if node == nil || node.Kind == 0 {
		return nil
	}

	if node.Kind != yaml.MappingNode {
		if prefix == "" {
			return fmt.Errorf("expected mapping, got %s", node.ShortTag())
		}

		values[prefix] = cloneYAMLNode(node)

		return nil
	}

	for i := 0; i+1 < len(node.Content); i += 2 {
		key, value := node.Content[i], node.Content[i+1]
		if key.Kind != yaml.ScalarNode {
			return fmt.Errorf("expected scalar config key, got %s", key.ShortTag())
		}

		path := pathJoin(prefix, key.Value)
		// Keep both mappings and leaves. Leaves are used as active chart
		// defaults; mappings let validation distinguish intentional nested
		// override blocks from empty structured objects.
		values[path] = cloneYAMLNode(value)

		if comment := normalizeYAMLComment(key.HeadComment); comment != "" {
			comments[path] = comment
		}

		if value.Kind == yaml.MappingNode {
			if err := parseConfigOverrides(value, path, values, comments); err != nil {
				return err
			}
		}
	}

	return nil
}

func parseComments(node *yaml.Node, prefix string, comments map[string]string) error {
	if node == nil || node.Kind == 0 {
		return nil
	}

	if node.Kind != yaml.MappingNode {
		return fmt.Errorf("expected mapping, got %s", node.ShortTag())
	}

	for i := 0; i+1 < len(node.Content); i += 2 {
		key, value := node.Content[i], node.Content[i+1]
		if key.Kind != yaml.ScalarNode {
			return fmt.Errorf("expected scalar comment key, got %s", key.ShortTag())
		}

		path := pathJoin(prefix, key.Value)
		if value.Kind == yaml.MappingNode {
			if err := parseComments(value, path, comments); err != nil {
				return err
			}

			continue
		}

		if isNonEmptyString(value) {
			comments[path] = value.Value

			continue
		}

		if comment := normalizeYAMLComment(key.HeadComment); comment != "" && isCommentOnlyValue(value) {
			comments[path] = comment

			continue
		}

		return fmt.Errorf(
			"comment override %q must be a non-empty string, a comment-only value, or a mapping, got %s",
			path,
			value.ShortTag(),
		)
	}

	return nil
}

func isNonEmptyString(node *yaml.Node) bool {
	return node.Kind == yaml.ScalarNode && node.ShortTag() == yamlStringTag && node.Value != ""
}

func isCommentOnlyValue(node *yaml.Node) bool {
	return node.Kind == yaml.ScalarNode && (node.ShortTag() == yamlNullTag || (node.ShortTag() == yamlStringTag && node.Value == ""))
}

func renderConfigBlock(schema *schema, overrides *overrides) (string, error) {
	root, err := resolveSchemaNode(schema.root, schema.definitions)
	if err != nil {
		return "", err
	}

	var builder strings.Builder

	builder.WriteString(beginConfigMarker)
	builder.WriteByte('\n')
	builder.WriteString(generatedConfigComment)
	builder.WriteByte('\n')
	builder.WriteString("config:\n")

	for _, property := range root.properties {
		if err = renderProperty(&builder, schema, overrides, property.name, property.node, property.name, 2, false); err != nil {
			return "", err
		}
	}

	builder.WriteString(endConfigMarker)
	builder.WriteByte('\n')

	return builder.String(), nil
}

func renderProperty(
	builder *strings.Builder,
	schema *schema,
	overrides *overrides,
	name string,
	node *schemaNode,
	path string,
	indent int,
	ignoredByAncestor bool,
) error {
	if overrides.omit[path] {
		return nil
	}

	resolved, err := resolveSchemaNode(node, schema.definitions)
	if err != nil {
		return fmt.Errorf("failed to resolve schema node %q: %w", path, err)
	}

	if resolved.isStructuredObject() {
		// Structural keys stay uncommented so users only need to uncomment the
		// leaf they want to set. The chart template later prunes null-only
		// branches before writing the Omni config Secret.
		activeDescendant, activeDescendantErr := hasActiveDescendant(schema, overrides, resolved, path)
		if activeDescendantErr != nil {
			return activeDescendantErr
		}

		if !activeDescendant && !ignoredByAncestor {
			// helm-docs would otherwise document this active YAML key as a nil
			// value. Ignore only fully inactive structural branches; branches
			// with active descendants must remain visible to helm-docs.
			writeComment(builder, indent, "@ignored")
		}

		writeComment(builder, indent, overrides.comment(path, resolved.description))
		writeKey(builder, indent, false, name, "")

		for _, property := range resolved.properties {
			childPath := path + pathSeparator + property.name
			if err = renderProperty(
				builder,
				schema,
				overrides,
				property.name,
				property.node,
				childPath,
				indent+2,
				ignoredByAncestor || !activeDescendant,
			); err != nil {
				return err
			}
		}

		return nil
	}

	value := overrides.example(path, resolved.defaultValue)
	comment := overrides.comment(path, resolved.description)
	commented := true

	if overrides.values[path] != nil {
		// Only active chart defaults get the helm-docs marker. Commented-out
		// schema examples remain visible in values.yaml without becoming README
		// table rows.
		value = overrides.values[path]
		commented = false
		comment = helmDocsComment(comment)
	}

	if value == nil {
		value = placeholderValue(resolved)
	}

	writeComment(builder, indent, comment)

	if err = writeValue(builder, indent, commented, name, value); err != nil {
		return fmt.Errorf("failed to render %q: %w", path, err)
	}

	if !commented {
		if err = writeActiveExample(builder, indent, name, overrides.examples[path]); err != nil {
			return fmt.Errorf("failed to render example for %q: %w", path, err)
		}
	}

	return nil
}

func (node *schemaNode) isStructuredObject() bool {
	return node.typ == schemaObjectType && node.additionalProperties == nil && len(node.properties) > 0
}

func hasDescendant(nodes map[string]*yaml.Node, path string) bool {
	for nodePath := range nodes {
		if strings.HasPrefix(nodePath, path+pathSeparator) {
			return true
		}
	}

	return false
}

func hasActiveDescendant(schema *schema, overrides *overrides, node *schemaNode, path string) (bool, error) {
	if overrides.omit[path] {
		return false, nil
	}

	resolved, err := resolveSchemaNode(node, schema.definitions)
	if err != nil {
		return false, fmt.Errorf("failed to resolve schema node %q: %w", path, err)
	}

	if !resolved.isStructuredObject() {
		return overrides.values[path] != nil, nil
	}

	for _, property := range resolved.properties {
		childPath := path + pathSeparator + property.name

		active, activeErr := hasActiveDescendant(schema, overrides, property.node, childPath)
		if activeErr != nil {
			return false, activeErr
		}

		if active {
			return true, nil
		}
	}

	return false, nil
}

func (overrides *overrides) comment(path string, fallback string) string {
	if comment := overrides.comments[path]; comment != "" {
		return comment
	}

	return fallback
}

func (overrides *overrides) example(path string, fallback *yaml.Node) *yaml.Node {
	if example := overrides.examples[path]; example != nil {
		return example
	}

	return fallback
}

func helmDocsComment(comment string) string {
	comment = strings.TrimSpace(comment)
	if comment == "" {
		return ""
	}

	if strings.HasPrefix(comment, "--") {
		return comment
	}

	return "-- " + comment
}

func pathJoin(prefix string, name string) string {
	if prefix == "" {
		return name
	}

	return prefix + pathSeparator + name
}

func writeComment(builder *strings.Builder, indent int, comment string) {
	for _, line := range commentLines(comment) {
		builder.WriteString(strings.Repeat(" ", indent))

		if line == "" {
			builder.WriteString("#\n")
		} else {
			builder.WriteString("# ")
			builder.WriteString(line)
			builder.WriteByte('\n')
		}
	}
}

func writeKey(builder *strings.Builder, indent int, commented bool, name string, suffix string) {
	builder.WriteString(strings.Repeat(" ", indent))

	if commented {
		builder.WriteByte('#')
	}

	builder.WriteString(name)
	builder.WriteByte(':')
	builder.WriteString(suffix)
	builder.WriteByte('\n')
}

func writeValue(builder *strings.Builder, indent int, commented bool, name string, value *yaml.Node) error {
	value = documentNode(value)

	// The renderer is deliberately manual: yaml.v4 can preserve comments when
	// parsing, but it cannot emit "commented-out YAML" values in the shape
	// helm-docs expects for chart documentation.
	switch value.Kind {
	case yaml.SequenceNode:
		if len(value.Content) == 0 {
			writeKey(builder, indent, commented, name, " []")

			return nil
		}

		writeKey(builder, indent, commented, name, "")

		for _, item := range value.Content {
			scalar, err := formatScalar(item)
			if err != nil {
				return err
			}

			builder.WriteString(strings.Repeat(" ", indent))

			if commented {
				builder.WriteString("#  - ")
			} else {
				builder.WriteString("  - ")
			}

			builder.WriteString(scalar)
			builder.WriteByte('\n')
		}

		return nil
	case yaml.MappingNode:
		if len(value.Content) == 0 {
			writeKey(builder, indent, commented, name, " {}")

			return nil
		}

		writeKey(builder, indent, commented, name, "")

		for i := 0; i+1 < len(value.Content); i += 2 {
			if err := writeValue(builder, indent+2, commented, value.Content[i].Value, value.Content[i+1]); err != nil {
				return err
			}
		}

		return nil
	default:
		scalar, err := formatScalar(value)
		if err != nil {
			return err
		}

		writeKey(builder, indent, commented, name, " "+scalar)

		return nil
	}
}

func writeActiveExample(builder *strings.Builder, indent int, name string, example *yaml.Node) error {
	if example == nil {
		return nil
	}

	writeComment(builder, indent, "Example:")

	return writeValue(builder, indent, true, name, example)
}

func formatScalar(node *yaml.Node) (string, error) {
	node = documentNode(node)
	if node == nil {
		return yamlNullValue, nil
	}

	if node.Kind != yaml.ScalarNode {
		return "", fmt.Errorf("expected scalar, got %s", node.ShortTag())
	}

	switch node.ShortTag() {
	case yamlStringTag:
		return formatString(node.Value), nil
	case yamlNullTag:
		return yamlNullValue, nil
	case yamlBoolTag:
		return strings.ToLower(node.Value), nil
	case yamlIntTag, yamlFloatTag:
		return node.Value, nil
	default:
		if node.Tag == "" {
			return formatString(node.Value), nil
		}

		return "", fmt.Errorf("unsupported scalar tag %q", node.Tag)
	}
}

func formatString(value string) string {
	if value == "" {
		return `""`
	}

	if isPlainStringSafe(value) {
		return value
	}

	return strconv.Quote(value)
}

func isPlainStringSafe(value string) bool {
	if strings.TrimSpace(value) != value || strings.ContainsAny(value, "\r\n\t") {
		return false
	}

	switch strings.ToLower(value) {
	case yamlNullValue, "~", "true", yamlFalseValue, "yes", "no", "on", "off":
		return false
	}

	var decoded any
	if err := yaml.Unmarshal([]byte(value), &decoded); err == nil {
		// If YAML would read the plain scalar back as a bool, number, timestamp,
		// or null, quote it so Helm preserves the value as a string.
		if _, ok := decoded.(string); !ok {
			return false
		}
	}

	if strings.Contains(value, ": ") || strings.Contains(value, " #") {
		return false
	}

	first, _ := utf8Rune(value)
	switch first {
	case '-', '?', ':', '{', '}', '[', ']', ',', '&', '*', '#', '!', '|', '>', '\'', '"', '%', '@', '`':
		return false
	}

	for _, r := range value {
		if unicode.IsControl(r) {
			return false
		}
	}

	return true
}

func utf8Rune(value string) (rune, bool) {
	for _, r := range value {
		return r, true
	}

	return 0, false
}

func placeholderValue(node *schemaNode) *yaml.Node {
	switch node.typ {
	case "array":
		return &yaml.Node{Kind: yaml.SequenceNode, Tag: yamlSeqTag}
	case "boolean":
		return &yaml.Node{Kind: yaml.ScalarNode, Tag: yamlBoolTag, Value: yamlFalseValue}
	case "integer", "number":
		return &yaml.Node{Kind: yaml.ScalarNode, Tag: yamlIntTag, Value: "0"}
	case schemaObjectType:
		return &yaml.Node{Kind: yaml.MappingNode, Tag: yamlMapTag}
	default:
		return &yaml.Node{Kind: yaml.ScalarNode, Tag: yamlStringTag}
	}
}

func commentLines(comment string) []string {
	comment = strings.TrimSpace(comment)
	if comment == "" {
		return nil
	}

	rawLines := strings.Split(comment, "\n")
	lines := make([]string, 0, len(rawLines))

	for _, line := range rawLines {
		line = strings.TrimSpace(line)
		if line == "" {
			lines = append(lines, "")

			continue
		}

		lines = append(lines, wrapCommentLine(line)...)
	}

	return lines
}

func wrapCommentLine(line string) []string {
	if len(line) <= commentWrapColumn {
		return []string{line}
	}

	words := strings.Fields(line)
	if len(words) == 0 {
		return []string{""}
	}

	lines := []string{words[0]}

	for _, word := range words[1:] {
		last := lines[len(lines)-1]
		if len(last)+1+len(word) <= commentWrapColumn {
			lines[len(lines)-1] = last + " " + word
		} else {
			lines = append(lines, word)
		}
	}

	return lines
}

func replaceConfigBlock(values string, configBlock string) (string, error) {
	lines := strings.SplitAfter(values, "\n")

	start, end, err := configMarkerRange(lines)
	if err != nil {
		return "", err
	}

	var builder strings.Builder

	builder.WriteString(strings.Join(lines[:start], ""))
	builder.WriteString(configBlock)
	builder.WriteString(strings.Join(lines[end+1:], ""))

	return builder.String(), nil
}

func configMarkerRange(lines []string) (int, int, error) {
	beginIndexes := markerIndexes(lines, beginConfigMarker)
	endIndexes := markerIndexes(lines, endConfigMarker)

	if len(beginIndexes) != 1 || len(endIndexes) != 1 {
		return 0, 0, fmt.Errorf(
			"expected exactly one %q marker and one %q marker, got %d and %d",
			beginConfigMarker,
			endConfigMarker,
			len(beginIndexes),
			len(endIndexes),
		)
	}

	begin, end := beginIndexes[0], endIndexes[0]
	if begin >= end {
		return 0, 0, fmt.Errorf("%q marker must appear before %q marker", beginConfigMarker, endConfigMarker)
	}

	return begin, end, nil
}

func markerIndexes(lines []string, marker string) []int {
	var indexes []int

	for i, line := range lines {
		if strings.TrimRight(line, "\r\n") == marker {
			indexes = append(indexes, i)
		}
	}

	return indexes
}

func documentNode(node *yaml.Node) *yaml.Node {
	if node != nil && node.Kind == yaml.DocumentNode && len(node.Content) > 0 {
		return node.Content[0]
	}

	return node
}

func cloneYAMLNode(node *yaml.Node) *yaml.Node {
	if node == nil {
		return nil
	}

	cloned := *node

	if len(node.Content) > 0 {
		cloned.Content = make([]*yaml.Node, 0, len(node.Content))

		for _, child := range node.Content {
			cloned.Content = append(cloned.Content, cloneYAMLNode(child))
		}
	}

	if node.Alias != nil {
		cloned.Alias = cloneYAMLNode(node.Alias)
	}

	return &cloned
}

func scalarString(node *yaml.Node) string {
	if node == nil || node.Kind != yaml.ScalarNode {
		return ""
	}

	return node.Value
}

func normalizeYAMLComment(comment string) string {
	if comment == "" {
		return ""
	}

	lines := strings.Split(comment, "\n")
	normalized := make([]string, 0, len(lines))

	for _, line := range lines {
		line = strings.TrimSpace(line)
		line = strings.TrimPrefix(line, "#")
		line = strings.TrimPrefix(line, " ")
		normalized = append(normalized, line)
	}

	return strings.TrimSpace(strings.Join(normalized, "\n"))
}
