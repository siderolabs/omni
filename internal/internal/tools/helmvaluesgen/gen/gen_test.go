func TestRunReplacesConfigBlock(t *testing.T) {
	dir := t.TempDir()

	schemaPath := filepath.Join(dir, "schema.json")
	overridesPath := filepath.Join(dir, "overrides.yaml")
	valuesPath := filepath.Join(dir, "values.yaml")

	mustWrite(t, schemaPath, `{
  "type": "object",
  "properties": {
    "service": {
      "description": "Service contains service settings.",
      "$ref": "#/definitions/Service",
      "properties": {
        "endpoint": { "default": "0.0.0.0:8080" }
      }
    }
  },
  "definitions": {
    "Service": {
      "type": "object",
      "properties": {
        "endpoint": {
          "description": "Endpoint is the service endpoint.",
          "type": "string"
        },
        "enabled": {
          "description": "Enabled controls whether the service is enabled.",
          "type": "boolean",
          "default": true
        },
        "labels": {
          "description": "Labels are extra labels.",
          "type": "object",
          "additionalProperties": { "type": "string" }
        },
        "items": {
          "description": "Items is a generated list.",
          "type": "array",
          "items": { "type": "string" }
        },
        "numericString": {
          "description": "NumericString is still a string.",
          "type": "string",
          "default": "12345"
        },
        "nested": {
          "description": "Nested contains nested settings.",
          "type": "object",
          "properties": {
            "flag": {
              "description": "Flag controls nested behavior.",
              "type": "boolean",
              "default": false
            }
          }
        },
        "old": {
          "description": "Old is omitted by the chart.",
          "type": "string"
        }
      }
    }
  }
}
`)

	mustWrite(t, overridesPath, `config:
  service:
    endpoint: https://service.example.com
    items: []
omit:
  service:
    old: true
comments:
  service:
    endpoint: |-
      Chart-specific service endpoint.
    enabled: |-
      Enabled controls the chart service.
examples:
  service:
    items:
      - item-a
      - item-b
    labels:
      app: example
`)

	mustWrite(t, valuesPath, `before: true

# ---------- BEGIN helmvaluesgen config ----------
# Code generated by helmvaluesgen; DO NOT EDIT.
config:
  stale: value
# ---------- END helmvaluesgen config ----------

after: true
`)

	if err := gen.Run(schemaPath, overridesPath, valuesPath); err != nil {
		t.Fatal(err)
	}

	got, err := os.ReadFile(valuesPath)
	if err != nil {
		t.Fatal(err)
	}

	want := `before: true

# ---------- BEGIN helmvaluesgen config ----------
# Code generated by helmvaluesgen; DO NOT EDIT.
config:
  # Service contains service settings.
  service:
    # -- Chart-specific service endpoint.
    endpoint: https://service.example.com
    # Enabled controls the chart service.
    #enabled: true
    # Labels are extra labels.
    #labels:
      #app: example
    # -- Items is a generated list.
    items: []
    # Example:
    #items:
    #  - item-a
    #  - item-b
    # NumericString is still a string.
    #numericString: "12345"
    # @ignored
    # Nested contains nested settings.
    nested:
      # Flag controls nested behavior.
      #flag: false
# ---------- END helmvaluesgen config ----------

after: true
`

	if string(got) != want {
		t.Fatalf("unexpected values.yaml\nwant:\n%s\ngot:\n%s", want, got)
	}
}

func TestRunDoesNotIgnoreStructuralBranchWithActiveNestedLeaf(t *testing.T) {
	dir := t.TempDir()

	schemaPath := filepath.Join(dir, "schema.json")
	overridesPath := filepath.Join(dir, "overrides.yaml")
	valuesPath := filepath.Join(dir, "values.yaml")

	mustWrite(t, schemaPath, `{
  "type": "object",
  "properties": {
    "service": {
      "description": "Service contains service settings.",
      "type": "object",
      "properties": {
        "nested": {
          "description": "Nested contains nested settings.",
          "type": "object",
          "properties": {
            "flag": {
              "description": "Flag controls nested behavior.",
              "type": "boolean",
              "default": false
            }
          }
        }
      }
    }
  }
}
`)

	mustWrite(t, overridesPath, `config:
  service:
    nested:
      flag: true
`)

	mustWrite(t, valuesPath, `# ---------- BEGIN helmvaluesgen config ----------
# Code generated by helmvaluesgen; DO NOT EDIT.
config:
  stale: value
# ---------- END helmvaluesgen config ----------
`)

	if err := gen.Run(schemaPath, overridesPath, valuesPath); err != nil {
		t.Fatal(err)
	}

	got, err := os.ReadFile(valuesPath)
	if err != nil {
		t.Fatal(err)
	}

	if strings.Contains(string(got), "@ignored") {
		t.Fatalf("active nested leaf should keep structural parents visible to helm-docs:\n%s", got)
	}

	if !strings.Contains(string(got), "# -- Flag controls nested behavior.\n      flag: true") {
		t.Fatalf("active nested leaf was not rendered as a helm-docs value:\n%s", got)
	}
}

